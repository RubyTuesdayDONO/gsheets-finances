// https://github.com/es-shims/Object.entries/blob/master/implementation.js
if (!Object.entries) {
  Object.entries = function entries(O) {
    var obj = this;
    var entries = [];
    for (var key in obj) {
      if (obj.hasOwnProperty(key) && obj.propertyIsEnumerable(key)) {
        entries.push([key, obj[key]]);
      }
    }
    return entries;
  };
}


